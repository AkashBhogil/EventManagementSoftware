    public void showPendingPayment()
    {
         DefaultTableModel model= (DefaultTableModel) jTable3.getModel();
             while(model.getRowCount()>0)
         {
             model.removeRow(0);
         }
         try
         {
             Statement stmt=con.createStatement();
             ResultSet res=stmt.executeQuery("select * from payment");
             while(res.next())
             {
                 double tp=Double.parseDouble(res.getString(2));
                  double pp=Double.parseDouble(res.getString(3));
                  double rp=tp-pp;
                  if(rp>0)
                  {
                      Statement stmt1=con.createStatement();
                      ResultSet res1=stmt1.executeQuery("select mobile from event where event_id='"+res.getString(1)+"'");
                       res1.next();
                        Statement stmt2=con.createStatement();
                      ResultSet res2=stmt2.executeQuery("select name from customer_details where mobile='"+res1.getString(1)+"'");
                      res2.next();
                     
                       model.addRow(new Object[]{res.getString(1),res2.getString(1),res1.getString(1),res.getString(2),rp});
                       stmt1.close();
                       stmt2.close();
                  }
             }
             stmt.close();
             
         }catch(Exception e)
         {
             
         }
    }
