    public void showReport(String from,String to)
    {
        try
        {
            
            
             DefaultTableModel model= (DefaultTableModel) jTable1.getModel();
              while(model.getRowCount()>0)
         {
             model.removeRow(0);
         }
            Statement stmt=con.createStatement();
            ResultSet res=stmt.executeQuery("select * from event where date between '"+from+"' AND '"+to+"'");
            while(res.next())
            {
                Statement stmt1=con.createStatement();
                
                ResultSet res1=stmt1.executeQuery("select * from customer_details where mobile='"+res.getString(2)+"'");
                 Statement stmt2=con.createStatement();  
                ResultSet res2=stmt2.executeQuery("select * from payment where event_id='"+res.getString(1)+"'");
                 model.addRow(new Object[]{res.getString(1),res1.getString(2),res1.getString(1),res1.getString(4),res.getString(6),res.getString(5),res.getString(3),res.getString(4),res2.getString(2),res2.getString(3),res2.getString(4),res.getString(7)});
                 stmt1.close();
                 stmt2.close();
            }
            stmt.close();
            
        }catch(Exception e)
        {
            
        }
    }
    
    
